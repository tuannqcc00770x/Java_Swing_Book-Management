    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSigninActionPerformed
        // TODO add your handling code here:
        try {
        	String pubID = txtPubId.getText();
        	String pubName = txtPubName.getText();
        	String pubAddress = txtPubAddress.getText();
        	Publisher p = new Publisher(pubID, pubName, pubAddress);
        	if (!pubID.matches("[0-9]{4}")) {
        		JOptionPane.showMessageDialog(AddPub.this, "Publisher id must be 4 numbers");
        	} else if (pubID.equals("")||pubName.equals("")||pubAddress.equals("")) {
        		JOptionPane.showMessageDialog(AddPub.this, "All info must be filled!");
        	} else if (!new PublisherDAO().checkPub(p)) {
        		new PublisherDAO().addPub(p);
        		JOptionPane.showMessageDialog(AddPub.this, "Add publisher successfully!");
        		dispose();
        	} else {
        		JOptionPane.showMessageDialog(AddPub.this, "Publisher ID already exist!");
        	}
        }catch (Exception ex) {
        	ex.printStackTrace();
        	}
    }//GEN-LAST:event_btnSigninActionPerformed

